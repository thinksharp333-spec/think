CREATE POLICY "Public access" ON book_reviews FOR ALL USING (true);

-- Auto-calc average ratings trigger
CREATE OR REPLACE FUNCTION update_book_rating()
RETURNS TRIGGER AS $$
DECLARE
    target_book_id BIGINT;
BEGIN
    IF TG_OP = 'DELETE' THEN
        target_book_id := OLD.book_id;
    ELSE
        target_book_id := NEW.book_id;
    END IF;

    UPDATE books
    SET 
        avg_rating = (
            SELECT COALESCE(AVG(rating), 0)
            FROM book_reviews
            WHERE book_id = target_book_id
        ),
        review_count = (
            SELECT COUNT(*)
            FROM book_reviews
            WHERE book_id = target_book_id
        )
    WHERE id = target_book_id;
    
    IF TG_OP = 'DELETE' THEN
        RETURN OLD;
    ELSE
        RETURN NEW;
    END IF;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS trigger_update_book_rating ON book_reviews;
CREATE TRIGGER trigger_update_book_rating
AFTER INSERT OR UPDATE OR DELETE ON book_reviews
FOR EACH ROW
EXECUTE FUNCTION update_book_rating();
