-- Analytics Views
-- 1. Student Reading Aggregations
DROP VIEW IF EXISTS analytics_student_books;
CREATE OR REPLACE VIEW analytics_student_books AS
SELECT 
    user_id,
    u.name as student_name,
    u.school_id,
    u.grade,
    COUNT(DISTINCT book_id) as total_books_read,
    SUM(duration_seconds) as total_reading_time_seconds,
    SUM(pages_read) as total_pages_read
FROM reading_sessions rs
JOIN users u ON rs.user_id = u.id
GROUP BY user_id, u.name, u.school_id, u.grade;

-- 2. Top Books/Levels
DROP VIEW IF EXISTS analytics_top_books;
CREATE OR REPLACE VIEW analytics_top_books AS
SELECT 
    rs.book_id,
    rs.book_title,
    b.grade_level,
    COUNT(*) as session_count
FROM reading_sessions rs
LEFT JOIN books b ON rs.book_id::text = b.id::text
GROUP BY rs.book_id, rs.book_title, b.grade_level;

-- 3. School Stats
DROP VIEW IF EXISTS analytics_school_stats;
CREATE OR REPLACE VIEW analytics_school_stats AS
SELECT 
    u.school_id,
    s.school_name,
    s.district,
    s.taluka,
    COUNT(*) as total_sessions,
    COUNT(DISTINCT rs.book_id) as unique_books_read,
    COUNT(DISTINCT rs.user_id) as participating_students
FROM reading_sessions rs
JOIN users u ON rs.user_id = u.id
JOIN schools s ON u.school_id = s.id
GROUP BY u.school_id, s.school_name, s.district, s.taluka;

